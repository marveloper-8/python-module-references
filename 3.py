import math

# math arc cosine
print(math.acos(0.55))
print(math.acos(-0.55))
print(math.acos(0))
print(math.acos(1))
print(math.acos(-1))

# math inverse hyperbolic cosine
print(math.acosh(7))
print(math.acosh(56))
print(math.acosh(1))
print(math.acosh(2.45))

# return the arc sine of different numbers
print(math.asin(0.55))
print(math.asin(-0.55))
print(math.asin(0))
print(math.asin(1))
print(math.asin(-1))

# math inverse hypobolic sine
print(math.asinh(7))
print(math.asinh(56))
print(math.asinh(1))
print(math.asinh(2.45))

# math arc tangent
print(math.atan(0.39))
