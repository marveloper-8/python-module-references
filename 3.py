import math

# math arc cosine
print(math.acos(0.55))
print(math.acos(-0.55))
print(math.acos(0))
print(math.acos(1))
print(math.acos(-1))

# math inverse hyperbolic cosine
print(math.acosh(7))
print(math.acosh(56))
print(math.acosh(1))
print(math.acosh(2.45))

# return the arc sine of different numbers
print(math.asin(0.55))
print(math.asin(-0.55))
print(math.asin(0))
print(math.asin(1))
print(math.asin(-1))

# math inverse hypobolic sine
print(math.asinh(7))
print(math.asinh(56))
print(math.asinh(1))
print(math.asinh(2.45))

# math arc tangent
print(math.atan(0.39))
print(math.atan(67))
print(math.atan(-21))

# math arc tangent of y/x in radians
print(math.atan2(8, 5))
print(math.atan2(20, 10))
print(math.atan2(34, -7))

# math hyperbolic arctangent
print(math.atanh(0.59))
print(math.atanh(-0.12))

# math round number upward to nearest integer
print(math.ceil(1.4))
print(math.ceil(5.3))
print(math.ceil(22.6))
print(math.ceil(10.0))
print(math.ceil(-5.3))

# math combination
print(math.comb(7, 5))

# math return the value of the first parameter and the sign of the second parameter
print(math.copysign(4, -1))
print(math.copysign(-8, 97.21))
print(math.copysign(-43, -76))

# return the cosine of different numbers
print(math.cos(0.00))
print(math.cos(-1.23))
print(math.cos(10))
print(math.cos(3.14159265359))

# math hyperbolic cosine
print(math.cosh(1))
print(math.cosh(8.90))
print(math.cosh(1.52))
print(math.cosh(0))

# math convert radians to degrees
print(math.degrees(8.90))